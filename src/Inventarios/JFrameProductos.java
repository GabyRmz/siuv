    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        int p = JOptionPane.showConfirmDialog(null ,"Estas seguro de que querer Registrar el Producto", "Registrar", JOptionPane.YES_NO_OPTION);
        if(p==0){
        try
        {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            
            java.sql.Connection cnn = DriverManager.getConnection("jdbc:mysql://localhost/siuv", "root", "uv");
            java.sql.PreparedStatement stmt = cnn.prepareStatement("INSERT INTO PRODUCTO VALUES(?,?,?,?,?,?,?,?,?)");
            
            stmt.setInt(1, Integer.parseInt(this.txtCod.getText()));
            stmt.setFloat(2, Float.parseFloat(this.txtCost.getText()));
            stmt.setFloat(3, Float.parseFloat(this.txtPrec.getText()));
            stmt.setString(4, this.txtStat.getText());
            stmt.setString(5, this.txtNomPro.getText());
            stmt.setString(6, this.txtDesc.getText());
            stmt.setInt(7, Integer.parseInt(this.txtProve.getText()));
            stmt.setInt(8, Integer.parseInt(this.txtCat.getText()));
            stmt.setString(9, this.txtNumOrd.getText());
            
            stmt.execute();
            
            //cnn.commit();
            
            stmt.close();
            cnn.close();
            JOptionPane.showMessageDialog(this, "Producto Registrado","Aviso", JOptionPane.INFORMATION_MESSAGE);
            //Muestra mensaje de exito al Registrar un nuevo Producto
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Datos NO almacenados, verifique que el Código del proveedor y de la Categoría sean Correctos.","Aviso", JOptionPane.INFORMATION_MESSAGE);
            ex.printStackTrace();
        }
      }
    }//GEN-LAST:event_jButton8ActionPerformed

